/* Program to print "I will do my Assignment 100 times" using do while */

class DoWhilePrint {
    public static void main(String[] args) {
	    int number = 1;
		
		do {
		    System.out.println (" I will do my Assignment 100 times " + number);
			number++;
		}
		while(number <= 100);
	}
}
		